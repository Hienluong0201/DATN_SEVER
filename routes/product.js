    if (!mongoose.Types.ObjectId.isValid(productID)) {
      return res.status(400).json({ message: "productID kh√¥ng h·ª£p l·ªá." });
    }

    // l·∫•y s·∫£n ph·∫©m g·ªëc + ·∫£nh
